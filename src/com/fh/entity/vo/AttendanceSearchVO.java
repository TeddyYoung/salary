package com.fh.entity.vo;

public class AttendanceSearchVO {
	private String districtCode;
	
	private String stationCode;
	
	private String yearMonth;
	
	private String isSearchHide;
	
	private String organiseId;
	
	private String status;

	public String getDistrictCode() {
		return districtCode;
	}

	public void setDistrictCode(String districtCode) {
		this.districtCode = districtCode;
	}

	public String getStationCode() {
		return stationCode;
	}

	public void setStationCode(String stationCode) {
		this.stationCode = stationCode;
	}

	public String getYearMonth() {
		return yearMonth;
	}

	public void setYearMonth(String yearMonth) {
		this.yearMonth = yearMonth;
	}

	public String getIsSearchHide() {
		return isSearchHide;
	}

	public void setIsSearchHide(String isSearchHide) {
		this.isSearchHide = isSearchHide;
	}

	public String getOrganiseId() {
		return organiseId;
	}

	public void setOrganiseId(String organiseId) {
		this.organiseId = organiseId;
	}

	public String getStatus() {
		return status;
	}

	public void setStatus(String status) {
		this.status = status;
	}
	
}
